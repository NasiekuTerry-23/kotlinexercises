fun main(){

    var num1 = 12
    var num2 = 8

    // Performing the Calculations
    var addition = num1 + num2

    println("Addition: $num1 + $num2 = $addition")

    var substraction = num1 - num2

    println("Subtraction: $num1 - $num2 = $substraction")

    var multiplication = num1 * num2

    println("Multiplication: $num1 * $num2 = $multiplication")

    var division = num1 / num2

    println("Division: $num1 / $num2 = $division")

    var module = num1 % num2

    println("Modulus: $num1 % $num2 = $module")

}